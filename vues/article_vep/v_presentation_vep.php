<br>
<div class="container" style='text-align: center'>
    <img width="500" src='images/cv13.png'>
    <p class='vep'><span style='font-weight: bold'><u>Objectif</u></span> : Aide au placement durable en entreprise avec un coaching renforcé
        et rapide sur 1 à 5 rdv individuels maximum pour un CDD de 6 mois minimum
        (incluant les contrats d’alternance) ou CDI et participation à des opérations
        collectives de pré-recrutement <br> <br>
        <span style='font-weight: bold'><u>Cible</u></span> : jeunes ayant déjà défini et validé leur projet professionnel à la base ou
        sur un autre dispositif au préalable <br><br>
        Valoriser son expérience est un coup de pouce pour des jeunes qui bénéficient
        de + ou - 6 mois d’expérience et qui ont une idée précise de leur projet et
        souhaitent durablement aller vers l’emploi <br> <br>
        Cette action ne se destine donc pas aux jeunes qui souhaitent une poursuite
        d’études ou qui n’ont pas défini leur projet    
    </p>
