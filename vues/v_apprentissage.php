    <h2 class="app" style="text-align: center">En Ile-de-France, l’Etat et la Région se mobilisent, aux côtés des jeunes, des entreprises et des CFA, pour développer les formations en apprentissage.</h2>
    <h3 class='titreh3'>L’apprentissage, un enjeu important pour l’Ile-de-France</h3>
    <p class='app'>En 2017, près de 86 000 jeunes franciliens suivent une formation en apprentissage. La volonté de la Région Ile-de-France de développer l'apprentissage, en portant à 100 000 le nombre d'apprentis à l'horizon 2021, s'inscrit dans le cadre d’une politique nationale pour laquelle ont été fixés des objectifs chiffrés. <u><a style="color:#AE86BB" href='https://www.economie.gouv.fr/files/files/import/feuilleteur/sources/indexPop.htm'>Le Pacte national pour la croissance</a></u>, la compétitivité et l’emploi prévoit en effet de favoriser l’embauche de jeunes en apprentissage dans les PME, avec l’objectif de 500 000 apprentis en France en 2017. <br><br>
        Le développement de l’apprentissage est un enjeu national et régional pour la <b>formation et l’emploi des jeunes</b>. En janvier 2018, le Rapport pour le développement de l’apprentissage, piloté par Sylvie Brunet avec l’appui de France Stratégie, rappelle que « l’apprentissage améliore l’insertion professionnelle des jeunes par rapport à la voie scolaire à court et moyen terme ». <br><br>
        Ce constat, tout particulièrement vrai pour les niveaux IV et V, vaut pour l’ensemble des niveaux de certification. Selon le Céreq, le taux d’emploi des apprentis diplômés du baccalauréat 3 ans après leur sortie du système éducatif s’établit à près de 90% contre moins de 65% pour les diplômés de l’enseignement professionnel sous statut scolaire. Parce qu’il constitue une première expérience professionnelle, l’apprentissage est un élément qui favorise un accès plus rapide à un emploi durable, ainsi qu’à un meilleur niveau de salaire pour les sortants de l’enseignement supérieur. <br><br>
        En Ile-de-France, pour relever le défi du développement de l’apprentissage, plusieurs acteurs se mobilisent, notamment : <br><br>
    <ul class="app">  
        <li>la <b>Région</b>, qui signe les conventions portant sur la création des CFA (Centres de formation des apprentis) ; définit, en concertation avec l’Etat, l’offre de formation des CFA ; assure l’équilibre financier des CFA ; aide les apprentis ; définit le montant et verse la prime aux employeurs d’apprentis. </li>
        <li>  l’<b>Etat</b>, qui assure le contrôle pédagogique des formations en apprentissage (ministères de l’Education nationale, de l’Enseignement supérieur et de l’Agriculture) ; prépare et coordonne les mesures législatives encadrant l’apprentissage (ministère du Travail) ; organise la publication des listes d’habilitation des formations donnant droit à la taxe d’apprentissage (Préfet de région) ; </li>
        <li>   les <b>CFA</b>, qui forment les apprentis (enseignement général, technologique ou professionnel, parfois en partenariat avec un lycée professionnel, une université, une école d’ingénieurs, etc.) et assurent le lien avec l’entreprise et le maître d’apprentissage ;</li>
        <li>    les <b>entreprises</b>, également formatrices des apprentis, et contributrices du financement de l’apprentissage (via la taxe d’apprentissage). 
    </ul> 
</p>                   
<h4 class='titreh3'>Trouver une formation en apprentissage en Ile-de-France</h4>
<p class='app'>
    La Région a mis en place un <u><a style="color:#AE86BB; "href="https://www.iledefrance.fr/emploi-formation/apprentissage">portail internet</a></u> dédié à l’apprentissage en Ile-de-France. Il permet de rechercher une formation par secteur professionnel, département et niveau de certification. Le site affiche également les événements des CFA et les places disponibles en formation
</p>
