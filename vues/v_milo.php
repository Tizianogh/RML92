<h1 class="uk-heading-line uk-text-center"><span>Parcours d’intégration par l’acquisition de la langue</span></h1>
<br>
<div class="container" style=''>

    <p class='vep'>Le PIAL (Parcours d’intégration par l’acquisition de la langue) constitue un accompagnement d’une durée de 3 à 6 mois (maximum). Il est possible de modifier la date de fin prévisionnelle de cette phase initialement prévue, pour la prolonger ou la réduire en respectant la durée minimale et maximale (entre 3 et 6 mois). C’est une des 5 phases possibles dans le cadre de l’accompagnement du  » PACEA «.<br>

        Cette phase est réservée aux jeunes étrangers extra-européens en situation régulière (dont les primos-arrivants incluants les bénéficiaires d’une protection internationale) de 16 à 26 ans qui ne disposent pas du niveau minimal de maîtrise du français leur permettant une entrée dans les dispositifs d’insertion socio-professionnelle de droit commun (Garantie jeunes, E2C, EPIDE, …) et à fortiori un accès au marché du travail<br>

        Points de vigilance :
<ul class='vep'>
    <li> La phase « PIAL » ne doit pas chevaucher d’autres phases du PACEA</li>
    <li> La phase « PIAL » doit avoir une durée de 3 à 6 mois</li>
    <li> L’objectif : « Obtenir un niveau minimal de maîtrise de la langue française » doit systématiquement être renseigné dans cette phase</li>
    <li>Une des 4 règles spécifiques du PIAL est « la mise en place obligatoire d’une formation linguistique durant la phase PIAL »</li>
    <li> L’allocation PIAL (comme l’allocation PACEA) n’est pas proratisée. Elle relève de l’évaluation du professionnel qui la mobilise. Le montant maximum peut donc être versé, même si le jeune entre en cours de mois.</li>
    <li> Une fois l’allocation mobilisée dans le cadre de la phase PIAL épuisée il n’y a pas de possibilité de délivrer d’autre allocation PACEA au cours de la même période PACEA.</li>
    <li>  La demande d’allocation dans le cadre de la GJ prime sur la demande d’allocation PIAL si elle est demandée sur le même mois</li>
    <li>  Il convient d’être vigilant sur les situations ou dispositifs d’accompagnement mobilisés qui devront être renseignées car ils feront l’objet d’une observation de 1 jour à 1 mois après la fin de la phase PIAL</lI>
</ul> </p>
