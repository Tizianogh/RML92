<h2 class="titreh3" style="text-align: center">Appel à projets « Prépa apprentissage # demarretastory »  </h2>

<div class="container app">
    <p>Dans sa logique de développement de l’apprentissage, le gouvernement a fait ressortir un large consensus sur la nécessité d’un « sas » qui permettrait à tout jeune de bénéficier d’un accompagnement en fonction de ses besoins et permettant de faciliter l’accès à l’apprentissage et de sécuriser le parcours des apprentis.</p>
    <p>L’appel à projets « Prépa apprentissage # demarretastory » publié le 12 novembre 2018 constitue une expérimentation nationale mise en œuvre sur les territoires avant la mise en place de la préparation à l’apprentissage prévue par l’article 4 de la loi n° 2018-771 du 5 septembre 2018 pour la liberté de choisir son avenir professionnel et accompagnant la réforme de l’apprentissage.</p> 
    <p>Les projets proposés devront permettre d'offrir aux jeunes un accompagnement spécifique les préparant à intégrer une formation en alternance exigeante, à sécuriser leur parcours d'apprentis et à intégrer le monde de l'entreprise. Il est notamment attendu des projets qu’ils proposent  :</p>
    <ul>
        <li> d’identifier les compétences et les connaissances du jeune, de développer ses pré-requis relationnels et de sécuriser son entrée en contrat d’apprentissage ;</li>
        <li>  une adaptation de sa durée à la situation du jeune et à son projet professionnel ; </li>
        <li>  d’accompagner l’entreprise dans le recrutement du jeune et dans les démarches administratives liées au recrutement d’alternants ; </li>
        <li>  d’attirer les jeunes vers les emplois en tension (secteurs ou métiers à forts besoins de recrutement sur un territoire donné) ; </li>
        <li> de contribuer à la réduction du taux de rupture des contrats d’apprentissage. </li>
    </ul>
    <p >Les publics cibles sont les jeunes, avec une préoccupation pour les résidents dans les quartiers prioritaires de la politique de la ville ou dans les zones rurales à revitaliser (ZRR), les NEETS et les jeunes en situation de handicap.</p>
    <p > L’appel à projets sera ouvert <b>jusqu’au 15 mai 2019</b> avec une 1re vague levée au <b>10 décembre 2018</b>.
    <p> L’appel à projets  « Prépa Apprentissage #demarretastory » , ci-joint, est publié sur le site de consultation des investissements d’avenir de la Caisse des Dépôts.    <a href="https://cdcinvestissementsdavenir.achatpublic.com">https://cdcinvestissementsdavenir.achatpublic.com</a></p>
    <p> Les candidats pourront retirer le cahier des charges, le dossier de candidature et les annexes financières à partir de ce même site et pourront également poser leurs questions auxquelles une réponse sera apportée dans les meilleurs délais.</p>


