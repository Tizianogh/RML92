    <h3 class='titreh3'> L’ASSOCIATION  DÉPARTEMENTALE  DES  MISSIONS  LOCALES : UNE  SYNERGIE    ALTO  SEQUANAISE</h3>
    <p class='p'>        Créée à l’initiative des missions locales en 2008, l’association RML92 réunit les 9 missions locales du territoire. Elle a pour but de promouvoir le réseau, l’action des missions locales et de réaliser des actions à l’échelle départementale. <br> <br>
        
    Les missions locales sont  les interlocutrices des entreprises du territoire. Elle facilitent la mise en relation entre les jeunes altoséquanais et le tissu économique locale : opérations de recrutement, aide à l’embauche, actions d’adaptation à l’emploi, suivi dans l’emploi… </p>
  
    <h3 class='titreh3'> LE  RÉSEAU  EMPLOI DES JEUNES  DE  16 À   25 ANS  DANS  LES HAUTS  DE SEINE </h3>
   
    <ul class='ul'>
        <li>Les <b>9 Missions Locales</b>  interviennent avec l’ensemble des acteurs de  leur territoire</li>
        <li>Les <b>25 lieux d’accueil et  permanences délocalisés</b> sur <b>36 communes</b>  offrent un accueil de  proximité, veillent particulièrement à sa cohérence et à son organisation.</li>
        <li><b>220 professionnels </b> travaillent au sein des missions locales. Les  équipes pluridisciplinaires agissent en collaboration avec les acteurs  de leur territoire sur plu-  sieurs thématiques : emploi, orientation, formation, logement, santé,  mobilité...</li>

        
    </ul>
    <h3 class='titreh3'>LES MISSIONS LOCALES : LE SERVICE PUBLIC DE L’EMPLOI DES JEUNES </h3>
 
    <h4 class='titreh4'><b>Accompagner les jeunes dans leur projet social et professionnel</b></h4>
   
    <p class='p'>Elles exercent une mission de service public de proximité avec un objectif essentiel: permettre à tous les jeunes âgés de 16 à 25 ans de surmonter les difficultés qui font obstacle à leur insertion sociale et professionnelle. <br>
    Elles les informent, orientent et accompagnent en construisant avec eux     leurs parcours personnalisés vers l’emploi.
    </p>
    <h4 class='titreh4'><b>Développer localement de nouvelles solutions d’insertion</b></h4>
 
    <p class='p'>
        Elles favorisent la concertation entre les différents partenaires publics, économiques et associatifs. Elles apportent ainsi leur concours à l’offre de services pour l’insertion professionnelle et sociale des jeunes.
    </p>
    <h4 class='titreh4'><b>Mettre en œuvre des politiques publiques en faveur de l’emploi des jeunes</b></h4>
  
    <p class='p'>
        
Elles contribuent à l’élaboration et la mise en œuvre d’une politique locale concertée d’insertion des jeunes.
    </p>
