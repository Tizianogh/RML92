<p class='vep'>La Direccte

    <b>La Direction Régionale des Entreprises, de la Concurrence, de la Consommation, du Travail et de l’Emploi.</b>

La DIRECCTE, placée sous l’autorité du préfet de région, assure le pilotage des politiques publiques conduites par le ministère du travail, de l’emploi et de la santé, et le ministère de l’économie, des finances et de l’industrie.
</p>
