<p class="vep">
    La Région Ile-de-France

    La Région Ile-de-France est l’assemblée délibérante qui gère les affaires de la Région. Elle exerce des compétences notamment dans le domaine de l’action économique. <br>
De la formation à l’emploi, en passant par les transports régionaux, le sport ou encore la culture, la Région s’adresse à tous, toutes générations confondues. Son action structure durablement le territoire tout en accompagnant votre quotidien.
</p>
