<p class='vep'>
    <b> Les Missions Locales ont tissé de nombreux partenariats pour accompagner les jeunes dans leurs projets socio-professionnels : Etat, Conseil Régional, Conseils Départementaux, structures intercommunales, villes, Pôle emploi, Education Nationale, acteurs socio-éducatifs associations, clubs de prévention, monde économique….</b><br>
Ce partenariat avec des acteurs d’horizons divers permet aux Missions Locales d’élaborer des réponses, en synergie, adaptées à la situation des jeunes et de leur proposer une intervention globale en matière d’accès à l’emploi, de formation, mais aussi de santé, logement, culture, sport, loisirs …
Forts de leurs compétences spécifiques, ces acteurs sont mobilisés autour de projets concrets et innovants au service des jeunes.
